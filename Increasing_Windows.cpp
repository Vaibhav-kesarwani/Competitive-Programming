static const ll MOD = 998244353;
static const int MAXN = 200000 + 5;

ll fact[MAXN], invFact[MAXN];

ll comp(int n, int r) {
    if (r < 0 || r > n) return 0;
    return fact[n] * invFact[r] % MOD * invFact[n - r] % MOD;
}

ll modPow(ll a, ll b) {
    ll res = 1;
    while (b) {
        if (b & 1) res = res * a % MOD;
        a = a * a % MOD;
        b >>= 1;
    }
    return res;
}

void solve() {
    int n, k; cin >> n >> k;

    ll ans = 0;

    for (int i = 1; i <= k; i++) {
        ll a = comp(n - i - 1, k - i);
        ll b = modPow(i, k);

        ans = (ans + b * a) % MOD;
    }

    cout << ans % MOD << endl;
}
 
